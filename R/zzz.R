# Tweak function body on package build; this is needed only on Windows
if (identical(tolower(Sys.info()[["sysname"]]), "windows")
    || identical(Sys.getenv("PARZER_DEBUG_LOCALE_WRAPPER"), "true")) {

  # Get current environment
  parzer_ns <- environment()

  for (nm in ls(envir = parzer_ns)) {
    o <- get0(nm, mode = "function", inherits = FALSE, envir = parzer_ns)
    if (is.null(o)) {
      next
    }

    body(o) <- body(o)

    # e.g. function(x) x
    if (!identical(typeof(body(o)), "language")) {
      next
    }

    # Assume a function generated by Rcpp is
    # 1. the first element is `{`
    # 2. the second element is `.Call(...)`
    if (length(body(o)) != 2L ||
        !identical(body(o)[[1]], quote(`{`)) ||
        !identical(body(o)[[2]][[1]], quote(.Call))) {
      next
    }

    # `orig_body` will be replaced as the actual function later.
    wrapped_body <- quote(withr::with_locale(c(LC_COLLATE = "C"), orig_body))
    # Wrap the content of `body(o)` with withr::with_locale()
    wrapped_body[[3]] <- body(o)[[2]]
    body(o) <- wrapped_body

    assign(nm, o, envir = parzer_ns)
  }
}
